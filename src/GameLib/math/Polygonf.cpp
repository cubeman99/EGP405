Polygonf::Polygonf()
{
}

Polygonf::Polygonf(const Rect2f& rect)
{
	m_vertices.push_back(rect.GetTopLeft());
	m_vertices.push_back(rect.GetTopRight());
	m_vertices.push_back(rect.GetBottomRight());
	m_vertices.push_back(rect.GetBottomLeft());
}


